public class Variabel {

    public static void main(String[] args) {

        String parameter1 = args[0];

        int parameter2 = Integer.parseInt(args[1]);

        String parameter3 = "Baris ini";

        int parameter4 = 4;

        boolean parameter5 = true;

        System.out.println(parameter1 + " adalah variabel dengan Tipe data String");

        System.out.println(parameter2 + " adalah variabel dengan Tipe data Integer");

        System.out.println(parameter3 + " adalah variabel dengan Tipe data String yang di tulis langsung di class");

        System.out.println(parameter4 + " adalah variabel dengan Tipe data Integer yang di tulis langsung di class");

        System.out.println(parameter5);

    }
}